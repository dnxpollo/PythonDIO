# Interfaces definem o que uma classe deve ou não fazer
# O conceito de interface é definir um contrato, onde são declarados métodos 
# e suas respectivas assinaturas
# em Python utilizamos classes abstratas para criar contratos
# classes abstratas não podem ser instanciadas
# ABC - Abstract Base Class
# funciona decorando os métodos da classe base em abstratos
# em seguida, registrando classes concretas como implementações de base abstrata
# um método se torna abstrato quando decorado com @abstractmethod
from abc import ABC, abstractmethod, abstractproperty


class ControleRemoto(ABC):
    @abstractmethod
    def ligar(self):
        pass

    @abstractmethod
    def desligar(self):
        pass

    @property
    @abstractproperty
    def marca(self):
        pass


class ControleTV(ControleRemoto):
    def ligar(self):
        print("Ligando a TV...")
        print("Ligada!")

    def desligar(self):
        print("Desligando a TV...")
        print("Desligada!")

    @property
    def marca(self):
        return "Philco"


class ControleArCondicionado(ControleRemoto):
    def ligar(self):
        print("Ligando o Ar Condicionado...")
        print("Ligado!")

    def desligar(self):
        print("Desligando o Ar Condicionado...")
        print("Desligado!")

    @property
    def marca(self):
        return "LG"


controle = ControleTV()
controle.ligar()
controle.desligar()
print(controle.marca)


controle = ControleArCondicionado()
controle.ligar()
controle.desligar()
print(controle.marca)