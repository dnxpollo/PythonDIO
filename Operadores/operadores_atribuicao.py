#Atribuição simples
saldo = 500
print(saldo)

#Atribuição com adição
saldo = 500
saldo += 200
print(saldo)

#Atribuição com subtração
saldo = 500
saldo -= 200
print(saldo)

#Atribuição com multiplicação
saldo = 500
saldo *= 100
print(saldo)

#Atribuição com divisão
saldo = 500
saldo /= 100
print(saldo)

#Atribuição com divisão inteira
saldo = 500
saldo //= 200
print(saldo)

#Atribuição com modulo
saldo = 500
saldo %= 3
print(saldo)

#Atribuição com exponenciação
saldo = 500
saldo **= 3
print(saldo)